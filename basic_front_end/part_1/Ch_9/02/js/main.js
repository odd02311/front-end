// String
let myName = "Test_Name";
let email = 'test@gamil.com';
let hello = `Hello ${myName}?!`;

console.log(myName);
console.log(email);
console.log(hello);

// Number
let number = 123;
let opacity = 1.57;

console.log(number);
console.log(opacity);

// Boolean
let checked = true;
let isShow = false;

console.log(checked);
console.log(isShow);

// Undefined
let undef;
let obj = { abc: 123 };

console.log(undef);
console.log(obj.abc);
console.log(obj.xyz);

// Null
let empty = null;

console.log(empty);

// Object
let user = {
  name: 'TO_Name',
  age: 'TO_Age',
  isValid: true
};

console.log(user);
console.log(user.name);
console.log(user.age);
console.log(user.isValid);

// Array
let fruits = ['Apple', 'Banana', 'Cherry'];

console.log(fruits[0]); // Apple
console.log(fruits[1]); // Banana
console.log(fruits[2]); // Cherry


